"""Dataset for JSON-encoded mid-IR absorbance spectra."""

from __future__ import annotations

import json
from pathlib import Path
from typing import Any, Optional

import torch
from torch.utils.data import Dataset

from src.utils.json_io import load_json_file


class IRSpectrumDataset(Dataset[dict[str, Any]]):
    """Load individual spectrum JSON files as PyTorch samples.

    Args:
        data_dir: Directory containing spectrum JSON files.
        split_index: Optional JSON split index generated by scripts/prepare_data.py.
        split: Split name to load when split_index is provided.
        spectrum_length: Required spectrum length after preprocessing.
        cache: Whether to cache parsed samples in memory.
    """

    def __init__(
        self,
        data_dir: str | Path,
        split_index: str | Path | None = None,
        split: str | None = None,
        spectrum_length: int = 460,
        cache: bool = False,
    ) -> None:
        self.data_dir = Path(data_dir)
        self.split_index = Path(split_index) if split_index else None
        self.split = split
        self.spectrum_length = spectrum_length
        self.cache = cache

        if self.split_index is not None:
            self.entries = self._load_split_entries(self.split_index, split)
        else:
            self.entries = [{"path": path} for path in sorted(self.data_dir.rglob("*.json"))]

        if not self.entries:
            raise ValueError(f"No spectrum JSON files found for split={split!r} in {self.data_dir}")

        self._cache: list[dict[str, Any] | None] = [None] * len(self.entries) if cache else []
        if cache:
            for index in range(len(self.entries)):
                self._cache[index] = self._load_entry(self.entries[index])

    def __len__(self) -> int:
        """Return the number of spectra in the dataset."""

        return len(self.entries)

    def __getitem__(self, index: int) -> dict[str, Any]:
        """Return one parsed spectrum sample."""

        if self.cache:
            cached = self._cache[index]
            if cached is None:
                cached = self._load_entry(self.entries[index])
                self._cache[index] = cached
            return cached
        return self._load_entry(self.entries[index])

    def _load_split_entries(self, split_index: Path, split: str | None) -> list[dict[str, Any]]:
        """Load split entries from a JSON split file."""

        if split is None:
            raise ValueError("split must be provided when split_index is used")
        if not split_index.exists():
            raise FileNotFoundError(f"Split index not found: {split_index}")

        with split_index.open("r", encoding="utf-8") as handle:
            payload = json.load(handle)
        if split not in payload:
            raise KeyError(f"Split {split!r} not present in {split_index}")

        entries: list[dict[str, Any]] = []
        for item in payload[split]:
            if isinstance(item, str):
                entries.append({"path": item})
            elif isinstance(item, dict):
                entries.append(dict(item))
            else:
                raise TypeError(f"Unsupported split entry type: {type(item)!r}")
        return entries

    def _resolve_path(self, entry: dict[str, Any]) -> Path:
        """Resolve a split entry path against data_dir when needed."""

        raw_path = entry.get("path") or entry.get("file") or entry.get("filepath")
        if raw_path is None:
            raise KeyError(f"Split entry is missing a path field: {entry}")
        path = raw_path if isinstance(raw_path, Path) else Path(str(raw_path))
        return path if path.is_absolute() else self.data_dir / path

    def _load_entry(self, entry: dict[str, Any]) -> dict[str, Any]:
        """Parse one JSON file and return tensors plus metadata."""

        path = self._resolve_path(entry)
        if not path.exists():
            raise FileNotFoundError(f"Spectrum file not found: {path}")

        payload = load_json_file(path)

        spectrum = payload.get("spectrum")
        if not isinstance(spectrum, dict):
            raise ValueError(f"Missing spectrum object in {path}")

        y_values = spectrum.get("y")
        if not isinstance(y_values, list):
            raise ValueError(f"Missing spectrum.y array in {path}")
        if len(y_values) != self.spectrum_length:
            raise ValueError(
                f"Expected spectrum length {self.spectrum_length}, got {len(y_values)} in {path}"
            )

        x_values = spectrum.get("x")
        if isinstance(x_values, list) and len(x_values) == len(y_values):
            x_tensor = torch.tensor(x_values, dtype=torch.float32)
        else:
            x_tensor = torch.arange(len(y_values), dtype=torch.float32)

        metadata = payload.get("metadata") if isinstance(payload.get("metadata"), dict) else {}
        sample_id = str(payload.get("id") or path.stem)
        group_id = str(
            entry.get("group_id")
            or metadata.get("cas_number")
            or metadata.get("compound_name")
            or payload.get("object_name")
            or sample_id
        )

        return {
            "id": sample_id,
            "group_id": group_id,
            "path": str(path),
            "spectrum": torch.tensor(y_values, dtype=torch.float32),
            "x": x_tensor,
            "metadata": metadata,
        }
